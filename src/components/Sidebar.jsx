function Sidebar({ currentView, onNavigate }) {
  const [expandedGroups, setExpandedGroups] = useState(() => {
    const initial = {}
    NAV_ITEMS.forEach(item => {
      if (item.children) {
        const isChildActive = item.children.some(child => child.key === currentView)
        if (isChildActive) initial[item.key] = true
      }
    })
    return initial
  })

  const toggleGroup = (key) => {
    setExpandedGroups(prev => ({ ...prev, [key]: !prev[key] }))
  }

  return (
    <aside className="sidebar">
      <div className="logo-section">
        <LogoIcon />
        <div className="logo-text">WMS Pro</div>
      </div>

      <nav className="nav-menu">
        {NAV_ITEMS.map((item) => {
          if (item.children) {
            const isExpanded = expandedGroups[item.key]
            const isChildActive = item.children.some(child => child.key === currentView)
            return (
              <div key={item.key} className="nav-group">
                <button
                  className={`nav-item nav-group-toggle ${isChildActive ? 'group-active' : ''}`}
                  onClick={() => toggleGroup(item.key)}
                >
                  {item.icon}
                  <span className="nav-text">{item.label}</span>
                  <svg
                    className={`nav-chevron ${isExpanded ? 'expanded' : ''}`}
                    width="16"
                    height="16"
                    viewBox="0 0 16 16"
                    fill="none"
                    xmlns="http://www.w3.org/2000/svg"
                  >
                    <path d="M4 6L8 10L12 6" stroke="#94A3B8" strokeWidth="1.5" strokeLinecap="round" strokeLinejoin="round"/>
                  </svg>
                </button>
                <div className={`nav-submenu ${isExpanded ? 'expanded' : ''}`}>
                  <div className="nav-submenu-inner">
                    {item.children.map((child) => (
                      <button
                        key={child.key}
                        className={`nav-item nav-subitem ${currentView === child.key ? 'active' : ''}`}
                        onClick={() => onNavigate(child.key)}
                      >
                        {child.icon}
                        <span className="nav-text">{child.label}</span>
                      </button>
                    ))}
                  </div>
                </div>
              </div>
            )
          }
          return (
            <button
              key={item.key}
              className={`nav-item ${currentView === item.key ? 'active' : ''}`}
              onClick={() => !item.disabled && onNavigate(item.key)}
            >
              {item.icon}
              <span className="nav-text">{item.label}</span>
            </button>
          )
        })}
      </nav>
    </aside>
  )
}

export default Sidebar
